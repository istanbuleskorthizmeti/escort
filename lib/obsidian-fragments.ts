// Basit bir Spintax Parser
function resolveSpintax(text: string, rand: () => number): string {
  const regex = /{([^{}]*)}/;
  let match;
  while ((match = regex.exec(text)) !== null) {
    const choices = match[1].split('|');
    const choice = choices[Math.floor(rand() * choices.length)];
    text = text.slice(0, match.index) + choice + text.slice(match.index + match[0].length);
  }
  return text;
}

export function getStitchedContent(seed: number, targetWords = 3000): string {
  const allSpintaxPool = [
    ...medicalSpintax,
    ...psychoSexologySpintax,
    ...lifestyleSpintax,
    ...globalAuthoritySpintax,
    ...taxonomySpintax,
    ...ruralSpintax,
    ...hotelSpintax,
    ...eventSpintax,
    ...constructionSpintax,
    ...carEnthusiastSpintax,
    ...affairSpintax,
    ...jewelerSpintax,
    ...localSportsSpintax
  ];
  
  const rand = () => {
    seed = (seed * 1103515245 + 12345) % 2147483648;
    return seed / 2147483648;
  };
  
  const randomizedHavuz = [...allSpintaxPool];
  for (let i = randomizedHavuz.length - 1; i > 0; i--) {
    const j = Math.floor(rand() * (i + 1));
    [randomizedHavuz[i], randomizedHavuz[j]] = [randomizedHavuz[j], randomizedHavuz[i]];
  }
  
  let content = "";
  let currentWords = 0;
  let index = 0;

  while (currentWords < targetWords) {
    const baseFragment = randomizedHavuz[index % randomizedHavuz.length];
    const resolvedSentence = resolveSpintax(baseFragment, rand);
    
    content += resolvedSentence + "\n\n";
    currentWords += resolvedSentence.split(" ").length;
    
    index += Math.floor(rand() * 3) + 1;
  }

  return content;
}
